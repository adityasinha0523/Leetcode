    public static int maximum69Number (int num) {
        List<Integer> list=new ArrayList<>();
        while(num>0){
            int temp=num%10;
            list.add(temp);
            num=num/10;
        }
        int solution=0;
        Collections.reverse(list);
        boolean valueChange=false;
        for (int i = 0; i < list.size(); i++) {
            int temp=list.get(i);
            if(temp==6 && valueChange==false){
                valueChange=true;
                temp=9;
            }
            solution=solution*10+temp;
        }
        return solution;
    }
