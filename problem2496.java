    public static int maximumValue(String[] strs) {
        int solution=0;
        for (int i = 0; i < strs.length; i++) {
            String s=strs[i];
            boolean flag=false;
            for (int j = 0; j < s.length(); j++) {
                if(Character.isAlphabetic(s.charAt(j))){
                    flag=true;
                }
            }
            if(flag==true){
                solution=Math.max(solution, s.length());
            }else{
                solution=Math.max(solution, Integer.parseInt(s));
            }
        }
        return solution;
    }
