    public static int xorOperation(int n, int start) {
        int solution=0;
        int[] arr=new int[n];
        arr[0]=start;
        n=n-1;
        int k=1;
        while(n!=0){
            arr[k]=start+2*k;
            k++;
            n--;
        }
        for (int i : arr) {
            solution=solution^i;
        }
        return solution;
    } 
