public static int countKConstraintSubstrings(String s, int k) {
    int countZero=0;
    int countOne=1;
    int solution=0;
    for (int i = 0; i < s.length(); i++) {
        if(s.charAt(i)=='0'){
            countZero++;
        }
        if(s.charAt(i)=='1'){
            countOne++;
        }
        
        for (int j = i+1; j < s.length(); j++) {
            if(s.charAt(j)=='0'){
                countZero++;
                solution++;
            }
            if(s.charAt(j)=='1'){
                countOne++;
                solution++;
            }
            if(countOne >k&&countZero >k){
                countOne=0;
                countZero=0;
                break;
            }
        }
    }
    return solution;
}
