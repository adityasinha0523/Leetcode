    public static int bitwiseComplement(int n) {
        String val=Integer.toBinaryString(n);
        StringBuilder sb=new StringBuilder();
        for (int i = 0; i < val.length(); i++) {
            if(val.charAt(i)=='0'){
                sb.append('1');
            }else{
                sb.append('0');
            }
        }
        int solution=0;
        String sol=sb.toString();
        int pow=1;
        for (int i = sol.length()-1; i >=0; i--) {
            int val1=Character.getNumericValue(sol.charAt(i));
            solution+=val1==1?Math.pow(2,pow):0;
            pow++;
        }
        return solution;
    }
