    public static int maxPower(String s) {
        int maxValue=0;
        int currentValue=1;
        int prevValue=s.charAt(0)-'a';
        
        for (int i = 1; i < s.length(); i++) {
            char c=s.charAt(i);
            int value=c-'a';
            if(prevValue==value){
                currentValue++;
            }
            else{
                maxValue=Math.max(maxValue, currentValue);
                currentValue=1;
                prevValue=value;
            }
        }
        return Math.max(maxValue,currentValue);
    }
