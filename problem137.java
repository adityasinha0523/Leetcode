    public static int singleNumber(int[] nums) {
        int solution=0;
        for (int i = 0; i < 32; i++) {
            int noOfOne=0;
            for (int j = 0; j < nums.length; j++) {
                int val=nums[j];
                if((val&1)==1){
                    noOfOne++;
                }
                val=val>>1;
                nums[j]=val;
            }
            if(noOfOne%3!=0){
                int value=(1<<i);
                solution+=value;
            }
        }
        return solution;
    }
