    public static int countBinarySubstrings(String s) {
        int res=0;
        int cur=1;
        int prev=0;
        for (int i = 1; i < s.length(); i++) {
            if(s.charAt(i)==s.charAt(i-1)){
                cur++;
            }else{
                res+=Math.min(cur, prev);
                prev=cur;
                cur=1;
            }
        }
        return res+Math.min(cur,prev);
    }
