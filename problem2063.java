    public static long countVowels(String word) {
        long solution=0l;
        int counter=0;
        for (int i = 0; i < word.length(); i++) {
            if(word.charAt(i)=='a'||word.charAt(i)=='e'||word.charAt(i)=='i'||word.charAt(i)=='o'||word.charAt(i)=='u'){
                counter++;
                solution+=counter;
            }
        }
        int first=0;
        while(first<word.length()){
            if(word.charAt(first)=='a'||word.charAt(first)=='e'||word.charAt(first)=='i'||word.charAt(first)=='o'||word.charAt(first)=='u'){
                counter=counter-1;
                solution+=counter;
            }else{
                solution+=counter;
            }
            first++;
        }
        return solution;
    }
